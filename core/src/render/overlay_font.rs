//! Embedded 5x7 bitmap font for the diagnostics overlay (Plan 0011).
//!
//! A deliberately-minimal, dependency-free debug primitive: just the glyphs the
//! numeric readout needs (digits, a decimal point, a space, and the unit labels
//! FPS / MS / MB). It is intentionally *not* the glyphon text renderer of Plan
//! 0008 / ADR-0009 — that is feature-gated and standalone-scoped, while this
//! overlay is core-drawn and must paint in the foobar plugin too (all-three
//! parity) without adding a dependency (NFR 4). Keep it self-contained so a
//! later migration to glyphon, if it ever reaches core + plugin builds, is a
//! single-file swap.

// Hot-path panic-denial pragma (Plan 0002 Phase 2; `render/` scan set). This
// file is pure data + a match, but the guard is uniform across the module tree.
#![deny(
    clippy::unwrap_used,
    clippy::expect_used,
    clippy::indexing_slicing,
    clippy::panic,
    clippy::unreachable
)]

/// Glyph cell width in font pixels.
pub const GLYPH_W: usize = 5;
/// Glyph cell height in font pixels.
pub const GLYPH_H: usize = 7;

/// The 7-row bitmap for `c`, top row first. Within a row, bit 4 (`0x10`) is the
/// leftmost column and bit 0 the rightmost. Unknown characters render blank, so
/// the caller can format freely and only the covered glyphs light up.
pub fn glyph(c: char) -> [u8; GLYPH_H] {
    match c {
        '0' => [0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E],
        '1' => [0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E],
        '2' => [0x0E, 0x11, 0x01, 0x06, 0x08, 0x10, 0x1F],
        '3' => [0x0E, 0x11, 0x01, 0x06, 0x01, 0x11, 0x0E],
        '4' => [0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02],
        '5' => [0x1F, 0x10, 0x1E, 0x01, 0x01, 0x11, 0x0E],
        '6' => [0x0E, 0x11, 0x10, 0x1E, 0x11, 0x11, 0x0E],
        '7' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08],
        '8' => [0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E],
        '9' => [0x0E, 0x11, 0x11, 0x0F, 0x01, 0x11, 0x0E],
        '.' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x0C, 0x0C],
        '/' => [0x01, 0x02, 0x02, 0x04, 0x08, 0x08, 0x10],
        ':' => [0x00, 0x0C, 0x0C, 0x00, 0x0C, 0x0C, 0x00],
        'F' => [0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10],
        'P' => [0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10],
        'S' => [0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E],
        'M' => [0x11, 0x1B, 0x15, 0x15, 0x11, 0x11, 0x11],
        'B' => [0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E],
        _ => [0x00; GLYPH_H],
    }
}
